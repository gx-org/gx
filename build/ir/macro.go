package ir

import (
	"go/ast"
)

// Macro is a function that takes a set of IR nodes as an input
// and returns a new set of IR nodes (or a compiler error).
// An example of such function is math/graph.Grad.
// The implementation is opaque to GX.
type Macro struct {
	MetaCore

	BuildSynthetic MacroImpl
}

type (
	// MacroElement is an element generated by calling a macro implementation.
	MacroElement interface {
		Element
		From() *Macro
	}

	// FuncASTBuilder builds GX functions programmatically.
	FuncASTBuilder interface {
		MacroElement
		BuildDecl(target PkgFunc) (*File, *ast.FuncDecl, bool)
		BuildBody(Fetcher, Func) (*ast.BlockStmt, bool)
	}

	// FuncAnnotator annotates a function.
	FuncAnnotator interface {
		Annotate(Fetcher, PkgFunc, *FuncCallExpr, []Element) bool
		ShortString() string
	}

	// MacroImpl is a builtin opaque function to produce an IR.
	MacroImpl func(file *File, call *FuncCallExpr, fn *Macro, args []Element) (MacroElement, error)

	// AnnotatorImpl is an annotation implementation.
	AnnotatorImpl func(Fetcher, *Annotator, PkgFunc, *FuncCallExpr, []Element) bool
)

// MacroCallExpr calls a macro.
type MacroCallExpr struct {
	// X is the call to the macro to generate the macro instance.
	X *FuncCallExpr
	// M is the macro instance used to generate the function.
	M FuncASTBuilder
	// F is the function generated by the macro.
	F Func
}

var (
	_ Callee   = (*MacroCallExpr)(nil)
	_ CallExpr = (*MacroCallExpr)(nil)
)

func (s *MacroCallExpr) node() {}

// Expr returns the expression AST.
func (s *MacroCallExpr) Expr() ast.Expr { return s.X.Expr() }

// Type returns the type of the function.
func (s *MacroCallExpr) Type() Type {
	return s.F.Type()
}

// Func returns the function generated by the macro.
func (s *MacroCallExpr) Func() Func {
	return s.F
}

// FuncType returns the concrete type of the function.
func (s *MacroCallExpr) FuncType() *FuncType {
	return s.F.FuncType()
}

// FuncCall returns the function call from the call.
func (s *MacroCallExpr) FuncCall() *FuncCallExpr {
	ftype := *s.X.Callee.FuncType()
	ftype.Results = &FieldList{
		List: []*FieldGroup{
			&FieldGroup{
				Type: TypeExpr(nil, s.F.FuncType()),
			},
		},
	}
	return &FuncCallExpr{
		Src:    s.X.Src,
		Callee: NewFuncValExpr(s.X, s.M.From()).NewFType(&ftype),
	}
}

// Node returns the node in the AST tree.
func (s *MacroCallExpr) Node() ast.Node { return s.Expr() }

// Call returns the source of the call.
func (s *MacroCallExpr) Call() *ast.CallExpr { return s.X.Src }

// ShortString returns a string representation of the expression calling the macro.
func (s *MacroCallExpr) ShortString() string {
	return s.X.String()
}

// SourceString returns GX code representing the call to the macro.
func (s *MacroCallExpr) SourceString() string {
	return s.String()
}

// String representation of the macro.
func (s *MacroCallExpr) String() string {
	return s.ShortString()
}
